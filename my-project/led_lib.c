#include <led_hdr.h>

void leds_init(void)
{
	/* Enable GPIOD clock. */
	/* Manually: */
	// RCC_AHB1ENR |= RCC_AHB1ENR_IOPDEN;
	/* Using API functions: */
	rcc_periph_clock_enable(RCC_GPIOD);

	/* Set GPIO12 (in GPIO port D) to 'output push-pull'. */
	/* Manually: */
	// GPIOD_CRH = (GPIO_CNF_OUTPUT_PUSHPULL << (((8 - 8) * 4) + 2));
	// GPIOD_CRH |= (GPIO_MODE_OUTPUT_2_MHZ << ((8 - 8) * 4));
	/* Using API functions: */
	gpio_mode_setup(GPIOD, GPIO_MODE_OUTPUT, GPIO_PUPD_NONE, GREEN | YELOW | BLUE | RED);

}

void leds_set(uint16_t data) {
	gpio_set(GPIOD, data);
}

void leds_write(uint16_t set) {
	uint16_t clean = 0;

	set = (set & 0xf) << LED_FIRST;

	gpio_clear(GPIOD, (0xf << LED_FIRST));
	gpio_set(GPIOD, set);
}

